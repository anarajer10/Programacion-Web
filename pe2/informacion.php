      <h2>Información General</h2>
      <p>
        <strong>Ludus Tempus</strong> es un club deportivo especializado en <em>disciplinas históricas y
          medievales</em>,
        donde se combina la práctica deportiva con el valor cultural de cada actividad. Nuestro objetivo es recuperar y
        difundir
        las artes marciales y los deportes tradicionales de distintas épocas y culturas, promoviendo una forma de vida
        activa,
        respetuosa y conectada con la historia.
      </p>
      <p>
        Entre nuestras disciplinas se encuentran la esgrima medieval, el tiro con arco tradicional, el jousting (justas
        a caballo),
        el bōjutsu, la lucha grecorromana, el soft combat y el hurling. Cada una de ellas se practica bajo la
        supervisión de monitores
        especializados y con el uso de equipamiento adaptado a distintos niveles de experiencia, desde principiantes
        hasta avanzados.
      </p>
      <p>
        Además de los entrenamientos regulares, organizamos talleres temáticos, exhibiciones, torneos y jornadas de
        recreación histórica
        en colaboración con otras asociaciones culturales y deportivas. Nuestro club es un espacio inclusivo y abierto a
        todas las edades,
        donde se fomenta el compañerismo, la disciplina y el respeto por las tradiciones a través de la práctica
        deportiva y el aprendizaje mutuo.
      </p>
      <p>
        En <strong>Ludus Tempus</strong> creemos que el deporte puede ser también una ventana al pasado, una forma de
        mantener vivas
        las costumbres y valores que han forjado nuestra historia. Te invitamos a conocernos y a formar parte de esta
        aventura única.
      </p>
